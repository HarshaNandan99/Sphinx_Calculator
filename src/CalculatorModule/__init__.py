"""

CalculatorModule
--------------------
This is a Module which has Classes for Calculator Operations.

"""


class Calculator(object):

    """Calculator Class which is responsible for performing basic calculator operations"""

    def add_numbers(num1, num2):
        """
            This is a string which adds two integers
            :return: result
        """
        return num1 + num2

    def subtract_numbers(num1, num2):
        """
            This is a string which subtracts two integers
            :return: result
        """
        return num1 - num2

    def multiply_numbers(num1, num2):
        """
            This is a string which multiplies two integers
            :return: result
        """
        return num1 * num2

    def divide_numbers(num1, num2):
        """
            This is a string which divides two integers
            :return: result
        """
        return num1 / num2
